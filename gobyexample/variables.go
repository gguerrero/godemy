package main

import "fmt"

func main() {

    var a = "initial"
    fmt.Println(a)

    var b, c int = 1, 2
    fmt.Println(b, c)

    var d = true
    fmt.Println(d)

    var i int
    var str string
    var bool bool
    fmt.Println(i)
    fmt.Println(str)
    fmt.Println(bool)

    i = 1
    str = "foo"
    bool = true
    fmt.Println(i)
    fmt.Println(str)
    fmt.Println(bool)

    f := "apple"
    fmt.Println(f)
}
